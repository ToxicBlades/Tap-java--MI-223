public class VectorOperations {
    public static double calculateMagnitude(Vector vector) {
        return vector.calculateMagnitude();
    }

    public static Vector add(Vector vector1, Vector vector2) {
        return vector1.add(vector2);
    }

    public static boolean areParallel(Vector vector1, Vector vector2) {
        return vector1.areParallel(vector2);
    }

    public static boolean arePerpendicular(Vector vector1, Vector vector2) {
        return vector1.arePerpendicular(vector2);
    }
}